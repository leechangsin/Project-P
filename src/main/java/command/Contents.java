package command;

public class Contents {

	String con_id;
	String writer;
	String title;
	String text;
	String video;
	String image;
	String hit_number;
	String like_number;
	String share_number;

	public String getCon_id() {
		return con_id;
	}

	public void setCon_id(String con_id) {
		this.con_id = con_id;
	}

	public String getWriter() {
		return writer;
	}

	public void setWriter(String writer) {
		this.writer = writer;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getVideo() {
		return video;
	}

	public void setVideo(String video) {
		this.video = video;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getHit_number() {
		return hit_number;
	}

	public void setHit_number(String hit_number) {
		this.hit_number = hit_number;
	}

	public String getLike_number() {
		return like_number;
	}

	public void setLike_number(String like_number) {
		this.like_number = like_number;
	}

	public String getShare_number() {
		return share_number;
	}

	public void setShare_number(String share_number) {
		this.share_number = share_number;
	}

}